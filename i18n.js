function biBlock(en, ar) {
  return `<span class="bi-en">${en}</span><span class="bi-ar">${ar}</span>`;
}

function applyBilingual() {
  document.querySelectorAll('[data-bi]').forEach(el => {
    const key = el.dataset.bi;
    const entry = T[key];
    if (!entry) return;

    if (el.classList.contains('marquee-item')) {
      el.innerHTML = biBlock(entry.en, entry.ar) + ' <span class="sep"></span>';
      return;
    }

    el.classList.add('bi-block');
    el.innerHTML = biBlock(entry.en, entry.ar);
  });

  document.querySelectorAll('[data-bi-html]').forEach(el => {
    const key = el.dataset.biHtml;
    const entry = T[key];
    if (!entry) return;
    el.classList.add('bi-block');
    el.innerHTML = biBlock(entry.en, entry.ar);
  });

  const ph = document.getElementById('ctaEmail');
  if (ph && T.cta_placeholder) ph.placeholder = T.cta_placeholder.en;
}

applyBilingual();

